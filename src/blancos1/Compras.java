    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tblProductos.getModel();
        totalcompra = totalcompra - Float.parseFloat(tblProductos.getValueAt(tblProductos.getSelectedRow(), 4).toString());
        modelo.removeRow(tblProductos.getSelectedRow());

        lblTotalCompra.setText("Total venta: "+String.valueOf(totalcompra));
        btnEliminar.setEnabled(false);
        if(tblProductos.getRowCount() < 1)
            btnRegistrar.setEnabled(false);
    }//GEN-LAST:event_btnEliminarActionPerformed

