    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        int i=0, filas = tblProductos.getRowCount();
        if(filas > 0){
            compra = Integer.parseInt(txtId.getText());
            c.createCompra(compra, txtIdProveedor.getText(), Login.emp.id , txtFecha.getText());
            DefaultTableModel modelo = (DefaultTableModel) tblProductos.getModel();
            while(i < filas){
                int detalle = Login.emp.getNextID("DetalleCompras","idDetalleCompra");
                dc.createDetalleCompra(detalle, compra ,Integer.parseInt(modelo.getValueAt(i, 0).toString()),Integer.parseInt(modelo.getValueAt(i, 2).toString()),Float.parseFloat((String) modelo.getValueAt(i,3)));
                
                i++;
            }
            JOptionPane.showMessageDialog(null, "Compra "+ compra +" registrada con "+ lblProveedor.getText() +", "+i+" Productos y un total de $"+ totalcompra);
            p.updateInventario();
            
            txtIdProveedor.setText("");
            lblProveedor.setText("Cliente: ");
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            txtFecha.setText(dtf.format(LocalDateTime.now()));
            totalcompra = 0;
            lblTotalCompra.setText(String.valueOf("Total: "+totalcompra));
            
            modelo.setRowCount(0);
        }else{
            JOptionPane.showMessageDialog(null, "La compra no tiene productos");

        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

