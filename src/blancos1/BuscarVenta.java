    private void tblVentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVentaMouseClicked
        DefaultTableModel modelo = (DefaultTableModel) tblDetalleVenta.getModel();
        modelo.setNumRows(0);
        DefaultTableModel modelo2 = (DefaultTableModel) tblVenta.getModel();
        int idVenta, idDetalle, producto, cantidad;
        float costounitario = 0, costo = 0, total=0;
        String descripcion="";
        ResultSet rs = dv.readDetalleVenta(modelo2.getValueAt(tblVenta.getSelectedRow(),0).toString());
        
        try{
            while(rs.next()){
                idVenta = rs.getInt("idVenta");
                idDetalle = rs.getInt("idDetalleVentas");
                producto = rs.getInt("idProducto");
                cantidad = rs.getInt("cantidad");
                
                ResultSet re = p.readProducto(rs.getString("idProducto"));
                try{
                    while(re.next()){
                        descripcion = re.getString("descripcion");
                        System.out.println(descripcion);
                        costounitario = re.getFloat("valorVenta");
                        costo = costounitario * cantidad;
                        total = total + costo;
                    }
                }catch(Exception ex){
                    System.out.println(ex);
                }
                modelo.addRow(new Object[]{idDetalle,idVenta,producto,descripcion,cantidad,costounitario,costo});
                
                int idCliente, idVendedor, idRepartidor;
                String fechaVenta, direccion, fechaEntrega, status;
                ResultSet rv = venta.readVenta(String.valueOf(idVenta));
                try{
                    while(rv.next()){
                        idVenta = rv.getInt("idVenta");
                        idCliente = rv.getInt("idCliente");
                        idVendedor = rv.getInt("idVendedor");
                        idRepartidor = rv.getInt("idRepartidor");
                        fechaVenta = rv.getString("fechaVenta");
                        direccion = rv.getString("direccionEntrega");
                        status = rv.getString("status");
                        fechaEntrega = rv.getString("fechaEntrega");
                        cmbStatus.setEnabled(true);
                        btnModificar.setEnabled(true);
                        cmbStatus.removeAllItems();
                        if(status.equals("Solicitado")){
                            cmbStatus.addItem("Solicitado");
                            cmbStatus.addItem("Enviado");
                            cmbStatus.addItem("Entregado");
                            cmbStatus.addItem("Cancelado");
                        }else if(status.equals("Enviado")){
                            cmbStatus.addItem("Enviado");
                            cmbStatus.addItem("Entregado");
                            cmbStatus.addItem("Cancelado");
                        }else if(status.equals("Entregado")||status.equals("Cancelado")){
                            cmbStatus.addItem(status);
                            cmbStatus.setEnabled(false);
                            btnModificar.setEnabled(false);
                        }
                        cmbStatus.setSelectedIndex(1);
                        
                        txtId.setText(String.valueOf(idVenta));
                        txtCliente.setText(String.valueOf(idCliente));
                        txtVendedor.setText(String.valueOf(idVendedor));
                        txtRepartidor.setText(String.valueOf(idRepartidor));
                        txtFechaVenta.setText(fechaVenta);
                        txtDireccion.setText(direccion);
                        txtFechaEntrega.setText(fechaEntrega);
                        txtTotal.setText(String.valueOf(total));
                        btnMostrar.setEnabled(true);
                    }
                }catch(Exception exc){
                    System.out.println();
                }
                
            }
            
            
        }catch(Exception exc){
            System.out.println(exc);
        }
    }//GEN-LAST:event_tblVentaMouseClicked

