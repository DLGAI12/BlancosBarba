    private void getVentas() {
        DefaultTableModel modelo = (DefaultTableModel) tblVenta.getModel();
        modelo.setNumRows(0);
        int idVenta, idCliente, idVendedor, idRepartidor;
        String fechaVenta, direccion, fechaEntrega, status;
        ResultSet rs = venta.readVenta(txtBusqueda.getText());
        try{
            while(rs.next()){
                idVenta = rs.getInt("idVenta");
                idCliente = rs.getInt("idCliente");
                idVendedor = rs.getInt("idVendedor");
                idRepartidor = rs.getInt("idRepartidor");
                fechaVenta = rs.getString("fechaVenta");
                direccion = rs.getString("direccionEntrega");
                fechaEntrega = rs.getString("fechaEntrega");
                status = rs.getString("status");
                modelo.addRow(new Object[]{idVenta,idCliente,idVendedor,idRepartidor,fechaVenta,direccion,fechaEntrega,status});
            }
        }catch(Exception exc){
            System.out.println(exc);
        }
        
    }

