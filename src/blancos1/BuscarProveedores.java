    private void getProveedores() {
        DefaultTableModel modelo = (DefaultTableModel) tblProveedores.getModel();
        modelo.setNumRows(0);
        int id;
        String nombre, telefono, correo;
        ResultSet rs = pr.readProveedor(txtBusqueda.getText());
        try{
            while(rs.next()){
                id = rs.getInt("idProveedor");
                nombre = rs.getString("nombreProveedor");
                telefono = rs.getString("telefonoProveedor");
                correo = rs.getString("correoProveedor");
                modelo.addRow(new Object[]{id,nombre,telefono,correo});
            }
        }catch(Exception exc){
            System.out.println("Error en getclientes "+exc);
        }
        
    }
