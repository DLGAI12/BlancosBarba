    private void tblProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProveedoresMouseClicked
        DefaultTableModel modelo = (DefaultTableModel) tblProveedores.getModel();
        int seleccion = tblProveedores.getSelectedRow();
        txtId.setText( modelo.getValueAt(seleccion, 0).toString());
        txtNombre.setText((String) modelo.getValueAt(seleccion, 1).toString());
        txtTelefono.setText((String) modelo.getValueAt(seleccion, 2).toString());
        txtCorreo.setText((String) modelo.getValueAt(seleccion, 3).toString());
    }//GEN-LAST:event_tblProveedoresMouseClicked

