    private void getClientes() {
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        modelo.setNumRows(0);
        int idCliente;
        String nombre, apellidos, telefono, correo;
        ResultSet rs = cliente.readCliente(txtBusqueda.getText());
        try{
            while(rs.next()){
                idCliente = rs.getInt("idCliente");
                nombre = rs.getString("nombreCliente");
                apellidos = rs.getString("apellidosCliente");
                telefono = rs.getString("telefonoCliente");
                correo = rs.getString("correoCliente");
                modelo.addRow(new Object[]{idCliente,nombre,apellidos,telefono,correo});
            }
        }catch(Exception exc){
            System.out.println("Error en getclientes "+exc);
        }
        
    }

