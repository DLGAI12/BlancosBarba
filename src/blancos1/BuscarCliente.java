    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        id = Integer.parseInt(modelo.getValueAt(tblClientes.getSelectedRow(), 0).toString());
        if(id != -1){
            btnSeleccionar.setEnabled(true);
            btnModificar.setEnabled(true);
            txtId.setText(String.valueOf(id));
            txtNombre.setText(modelo.getValueAt(tblClientes.getSelectedRow(), 1).toString());
            txtApellidos.setText(modelo.getValueAt(tblClientes.getSelectedRow(), 2).toString());
            txtTelefono.setText(modelo.getValueAt(tblClientes.getSelectedRow(), 3).toString());
            txtCorreo.setText(modelo.getValueAt(tblClientes.getSelectedRow(), 4).toString());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

