    private void getIvas(){
        DefaultTableModel modelo = (DefaultTableModel) tblIva.getModel();
        modelo.setRowCount(0);
        ResultSet rs = i.readIva(txtBusqueda.getText());
        int id = 0,porc;
        String inicio, fin ;
        try{
            while(rs.next()){
            id = rs.getInt("idIva");
            inicio = rs.getString("fechaaplicacion");
            fin = rs.getString("fechatermino");
            porc = rs.getInt("porcentaje");
            modelo.addRow(new Object[]{id,porc,inicio,fin});
            }
        }catch(Exception exc){
            System.out.println("Error en busqueda de producto "+exc);
        }
    }

